#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		l;
	int		i;
	char	*dest;

	l = 0;
	i = 0;
	while (src[l])
		l++;
	dest = malloc(sizeof(char) * (l + 1));
	if (!dest)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
