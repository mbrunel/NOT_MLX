#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*j;

	i = 0;
	j = (char*)s;
	while (n > 0)
	{
		j[i] = '\0';
		i++;
		n--;
	}
}
